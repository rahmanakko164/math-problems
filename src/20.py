def multiply_numbers(a: int, b: int) -> int:
    """
    Multiply two integers.
    
    Args:
        a (int): The first integer.
        b (int): The second integer.
        
    Returns:
        int: The product of the two integers.
    """
    return a * b

# Example usage
result = multiply_numbers(5, 3)
print(result) # Output: 15
